# Tuples in Python -- 

# Tuples are ordered collection of data items. 
# They store multiple items in a single variable. 
# Tuple items are separated by commas and enclosed within round brackets (). 
# Tuples are unchangeable(immutable) meaning we can not alter them after creation.

tuple1 = (1,2,2,3,5,4,6)
tuple2 = ("Red", "Green", "Blue")
print(tuple1)
print(tuple2)

details = ("Abhijeet", 18, "FYBScIT", 9.8)
print(details)


# Tuple Indexes -- Each item/element in a tuple has its own unique index. This index can be used to access any particular item from the tuple. The first item has index [0], second item has index [1], third item has index [2] and so on.

country = ("Spain", "Italy", "India",)
#            [0]      [1]      [2]


# Indexing is quite similar to lists

tup = (1)
print(type(tup))

tup = (1,)
print(type(tup))

